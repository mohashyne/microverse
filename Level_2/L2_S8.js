// Basic arithmetics in javascript

let n = 5;
console.log(n); //result 5

//n = n + 3;
//shorthand 1
n += 3; // this applies to all arithmetic operator(*, / , -, %).
console.log(n); //result 8;

//shorthand 2
//n += 1  is exactly the same as below, this shorthand below is mostly used for 1 only
n ++; // this applies to all arithmetic operator(*, / , -, %).
console.log(n); //result 9;

//many times and it will add 1 for each of the ++ we use
n ++;
n ++;
n ++;
n ++;
console.log(n); // result 13




//Subtract

let m = 8;
console.log(m); //result 8

//m = m - 3;
//shorthand 1
m -= 3; // this applies to all arithmetic operator(*, / , -, %).
console.log(m); //result 5;

//shorthand 2
//m -= 1  is exactly the same as below, this shorthand below is mostly used for 1 only
m --; // this applies to all arithmetic operator(*, / , -, %).
console.log(m); //result 4;

//many times and it will add 1 for each of the ++ we use
m --;
m --;
console.log(m); // result 2